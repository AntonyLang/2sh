export function normalizeEnglishGloss(value: string): string {
  return value
    .toLowerCase()
    .replace(/[_*]/g, " ")
    .replace(/\([^)]*\)/g, " ")
    .replace(/\[[^\]]*]/g, " ")
    .replace(/[!?.,:;]+/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

export function lookupMandarinFromEnglishGloss(value: string): string[] {
  const normalized = normalizeEnglishGloss(value);

  if (ENGLISH_GLOSS_TO_MANDARIN[normalized]) {
    return ENGLISH_GLOSS_TO_MANDARIN[normalized];
  }

  if (normalized.includes(",")) {
    const nested = normalized
      .split(",")
      .flatMap((segment) => ENGLISH_GLOSS_TO_MANDARIN[segment.trim()] ?? []);

    if (nested.length > 0) {
      return [...new Set(nested)];
    }
  }

  return [];
}
